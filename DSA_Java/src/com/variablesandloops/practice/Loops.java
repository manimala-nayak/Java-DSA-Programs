package com.variablesandloops.practice;

public class Loops {

	public static void main(String[] args) {
		//basically we'll use while when we dont know till when the loop will run and 
		//will be using for when we are aware of the number of loops
		
		int i=3;
		/*
		 * while(i<5) { System.out.println(i); i++; }
		 */
		
		for(i=3;i<5;i++)
		{
			System.out.println(i);
		}

	}

}
